package com.bestinfo.bean.business;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * 代销商游戏特权
 *
 * @author chenliping
 */
public class DealerPrivilege implements Serializable {

    /**
	 * 
	 */
	private static final long serialVersionUID = 7518994578012698856L;
	private String dealer_id;//代销商编号
    private Integer game_id;//游戏编号
    private BigDecimal service_proportion;//服务费比例
    private Integer game_permit;//游戏许可
    //代销商游戏特权列表页面中显示游戏名称所用字段
    private String game_name;//游戏名称

    /**
     * 代销商编号
     * @return the dealer_id
     */
    public String getDealer_id() {
        return dealer_id;
    }

    /**
     * 代销商编号
     * @param dealer_id the dealer_id to set
     */
    public void setDealer_id(String dealer_id) {
        this.dealer_id = dealer_id;
    }

    /**
     * 游戏编号
     * @return the game_id
     */
    public Integer getGame_id() {
        return game_id;
    }

    /**
     * 游戏编号
     * @param game_id the game_id to set
     */
    public void setGame_id(Integer game_id) {
        this.game_id = game_id;
    }

    /**
     * 服务费比例
     * @return the service_proportion
     */
    public BigDecimal getService_proportion() {
        return service_proportion;
    }

    /**
     * 服务费比例
     * @param service_proportion the service_proportion to set
     */
    public void setService_proportion(BigDecimal service_proportion) {
        this.service_proportion = service_proportion;
    }

    /**
     * 游戏许可
     * @return the game_permit
     */
    public Integer getGame_permit() {
        return game_permit;
    }

    /**
     * 游戏许可
     * @param game_permit the game_permit to set
     */
    public void setGame_permit(Integer game_permit) {
        this.game_permit = game_permit;
    }

    /**
     * 游戏名称
     * @return 
     */
    public String getGame_name() {
        return game_name;
    }

    /**
     * 游戏名称
     * @param game_name 
     */
    public void setGame_name(String game_name) {
        this.game_name = game_name;
    }

}
