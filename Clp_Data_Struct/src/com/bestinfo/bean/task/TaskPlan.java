package com.bestinfo.bean.task;

import java.io.Serializable;
import java.util.Date;

/**
 * 结算统计表--任务计划及执行结果
 *
 * @author zyk
 */
public class TaskPlan implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = -5773336589355364649L;
	/**
	 * 任务编号
	 */
	private Integer task_id;
	/**
	 * 计划编号
	 */
	private Integer plan_id;
	/**
	 * 计划名称
	 */
	private String plan_name;
	/**
	 * 结算数据范围开始时间
	 */
	private Date begin_date;
	/**
	 * 结算数据范围结束时间
	 */
	private Date end_date;
	/**
	 * 结算完成时间
	 */
	private Date stat_date;
	/**
	 * 计划完成状态
	 */
	private Integer plan_status;
	/**
	 * 用户编号
	 */
	private Integer user_id;
	/**
	 * 影响投注机数量
	 */
	private Integer terminal_count;
	/**
	 * 统计金额
	 */
	private Integer total_money;

	/**
	 * @return the task_id
	 */
	public Integer getTask_id() {
		return task_id;
	}

	/**
	 * @param task_id
	 *            the task_id to set
	 */
	public void setTask_id(Integer task_id) {
		this.task_id = task_id;
	}

	/**
	 * @return the plan_id
	 */
	public Integer getPlan_id() {
		return plan_id;
	}

	/**
	 * @param plan_id
	 *            the plan_id to set
	 */
	public void setPlan_id(Integer plan_id) {
		this.plan_id = plan_id;
	}

	/**
	 * @return the plan_name
	 */
	public String getPlan_name() {
		return plan_name;
	}

	/**
	 * @param plan_name
	 *            the plan_name to set
	 */
	public void setPlan_name(String plan_name) {
		this.plan_name = plan_name;
	}

	/**
	 * @return the begin_date
	 */
	public Date getBegin_date() {
		return begin_date;
	}

	/**
	 * @param begin_date
	 *            the begin_date to set
	 */
	public void setBegin_date(Date begin_date) {
		this.begin_date = begin_date;
	}

	/**
	 * @return the end_date
	 */
	public Date getEnd_date() {
		return end_date;
	}

	/**
	 * @param end_date
	 *            the end_date to set
	 */
	public void setEnd_date(Date end_date) {
		this.end_date = end_date;
	}

	/**
	 * @return the stat_date
	 */
	public Date getStat_date() {
		return stat_date;
	}

	/**
	 * @param stat_date
	 *            the stat_date to set
	 */
	public void setStat_date(Date stat_date) {
		this.stat_date = stat_date;
	}

	/**
	 * @return the plan_status
	 */
	public Integer getPlan_status() {
		return plan_status;
	}

	/**
	 * @param plan_status
	 *            the plan_status to set
	 */
	public void setPlan_status(Integer plan_status) {
		this.plan_status = plan_status;
	}

	/**
	 * @return the user_id
	 */
	public Integer getUser_id() {
		return user_id;
	}

	/**
	 * @param user_id
	 *            the user_id to set
	 */
	public void setUser_id(Integer user_id) {
		this.user_id = user_id;
	}

	/**
	 * @return the terminal_count
	 */
	public Integer getTerminal_count() {
		return terminal_count;
	}

	/**
	 * @param terminal_count
	 *            the terminal_count to set
	 */
	public void setTerminal_count(Integer terminal_count) {
		this.terminal_count = terminal_count;
	}

	/**
	 * @return the total_money
	 */
	public Integer getTotal_money() {
		return total_money;
	}

	/**
	 * @param total_money
	 *            the total_money to set
	 */
	public void setTotal_money(Integer total_money) {
		this.total_money = total_money;
	}

}
