package com.bestinfo.bean.openPrize;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 *
 * @author lvchangrong
 */
public class LuckyDetail implements Serializable{

    /**
	 * 
	 */
	private static final long serialVersionUID = -1182665737519623008L;
	private Integer classId;  //奖级
    private String className;//奖级名称
    private Integer totalNum;//全国中奖注数
    private Integer lotteryNum; // 风采系统中奖注数
    private BigDecimal stakePrize; //单注中奖金额

    /**
     * @return the totalNum
     */
    public Integer getTotalNum() {
        return totalNum;
    }

    /**
     * @param totalNum the totalNum to set
     */
    public void setTotalNum(Integer totalNum) {
        this.totalNum = totalNum;
    }

    /**
     * @return the lotteryNum
     */
    public Integer getLotteryNum() {
        return lotteryNum;
    }

    /**
     * @param lotteryNum the lotteryNum to set
     */
    public void setLotteryNum(Integer lotteryNum) {
        this.lotteryNum = lotteryNum;
    }

    /**
     * @return the stakePrize
     */
    public BigDecimal getStakePrize() {
        return stakePrize;
    }

    /**
     * @param stakePrize the stakePrize to set
     */
    public void setStakePrize(BigDecimal stakePrize) {
        this.stakePrize = stakePrize;
    }

    /**
     * @return the classId
     */
    public Integer getClassId() {
        return classId;
    }

    /**
     * @param classId the classId to set
     */
    public void setClassId(Integer classId) {
        this.classId = classId;
    }

    /**
     * @return the className
     */
    public String getClassName() {
        return className;
    }

    /**
     * @param className the className to set
     */
    public void setClassName(String className) {
        this.className = className;
    }

}
