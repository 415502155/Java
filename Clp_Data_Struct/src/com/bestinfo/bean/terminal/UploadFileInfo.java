package com.bestinfo.bean.terminal;

/**
 *
 * @author YangRong
 */
public class UploadFileInfo {
    private Integer returnCode;
    private String filePath;
    private Integer fileSize;
    private Integer recevSize;
    private String serverName;

    @Override
    public String toString() {
        return "UploadFileInfo{" + "returnCode=" + returnCode + ", filePath=" + filePath + ", fileSize=" + fileSize + ", recevSize=" + recevSize + ", serverName=" + serverName + '}';
    }

    public Integer getReturnCode() {
        return returnCode;
    }

    public void setReturnCode(Integer returnCode) {
        this.returnCode = returnCode;
    }

    public String getFilePath() {
        return filePath;
    }

    public void setFilePath(String filePath) {
        this.filePath = filePath;
    }

    public Integer getFileSize() {
        return fileSize;
    }

    public void setFileSize(Integer fileSize) {
        this.fileSize = fileSize;
    }

    public Integer getRecevSize() {
        return recevSize;
    }

    public void setRecevSize(Integer recevSize) {
        this.recevSize = recevSize;
    }

    public String getServerName() {
        return serverName;
    }

    public void setServerName(String serverName) {
        this.serverName = serverName;
    }
    
    
}
