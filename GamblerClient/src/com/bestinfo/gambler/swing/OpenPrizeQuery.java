            DateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            int actionID = ActionID.OPENPRIZE_DOWNLOAD;
            AppHeader head = new AppHeader();

            head.setType(Integer.parseInt(sendtype3.getText()));//head.setType(1);//发送方类型
            head.setAction(actionID);//协议编号
            head.setVersion(0);//version版本号(填0)
            head.setDealer_id(sendtype4.getText());//head.setDealer_id("111");//运营商ID
            head.setTerminal_id(Integer.parseInt(sendtype5.getText()));//head.setTerminal_id("111");//终端机号
            head.setMobile(sendtype7.getText());//手机号(填0)head.setMobile("15101105612");
            head.setPhone(sendtype6.getText());//电话号码(填0)   head.setPhone("15101105612");
            head.setSent_time(new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(new Date()));

            String strGame_ID = game_id.getText();
            if (strGame_ID.isEmpty()) {
                logger.error("游戏编码不可以为空！");
                return;
            }

            String strDraw_ID = draw_id.getText();
            if (strDraw_ID.isEmpty()) {
                logger.error("期号不可以为空！");
                return;
            }

            String strKeno_Draw_ID = keno_draw_id.getText();

            OpenPrizeQueryReq req = new OpenPrizeQueryReq();

            try {
                req.setGameId(Integer.parseInt(strGame_ID));
                req.setDrawId(Integer.parseInt(strDraw_ID));
                if (!strDraw_ID.isEmpty()) {
                    req.setKenoDrawId(Integer.parseInt(strKeno_Draw_ID));
                }
            } catch (Exception e) {
                logger.error("输入参数有误！" + e.getMessage());
            }

            POpenPrizeQueryClient pp = new POpenPrizeQueryClient();
            String s = pp.generateXML(head, req);
            System.out.println(s);

//            String sessionKey = (String) CacheManager.getCacheInfo("sessionKey").getValue();
//            logger.info("sessionKey is: " + sessionKey);
//            if (sessionKey == null) {
//                logger.info("sessionKey is null");
//            }
//            byte[] ret_key = NewSign.GetSign(s, "d:/gdclient.p12", "gdtest@2015", "cosmos", sessionKey);
            // byte[] ret_key = NewSign.GetS(requestxml, "d:/gdtestp12.cer", str_key);
            //byte[] ret_key = NewSign.GetSign(s, FilePath.getStorePath(), FilePath.getAliaspwd(), FilePath.getAlias(), str_key);
            //byte[] ret_key = NewSign.GetSign(s, FilePath.getStorePath(), FilePath.getAliaspwd(), FilePath.getAlias(), str_key);
            byte[] ret_key = NewSign.GetSign(s, StaticVariable.privateKey, StaticVariable.SESSION);

            head.setBody_sign(ret_key);
            System.out.println(s);

            s = pp.generateXML(head, req);

            //拼接发送XML
            String xml = "action=" + actionID + "&" + s;
            System.out.println(xml);

