package com.bestinfo.bean.encoding;

import java.io.Serializable;

/**
 * 应用类型
 *
 * @author lihui
 */
public class AppType implements Serializable {

    /**
     *
     */
    private static final long serialVersionUID = -1759494848091097425L;
    private Integer app_type;  //应用类型
    private String app_type_name; //应用类型名称
    private Integer work_status; //工作状态

    public Integer getApp_type() {
        return app_type;
    }

    public void setApp_type(Integer app_type) {
        this.app_type = app_type;
    }

    public String getApp_type_name() {
        return app_type_name;
    }

    public void setApp_type_name(String app_type_name) {
        this.app_type_name = app_type_name;
    }

    public Integer getWork_status() {
        return work_status;
    }

    public void setWork_status(Integer work_status) {
        this.work_status = work_status;
    }

}
