    private int check(String ticketnumSelect) {
        java.util.regex.Pattern pattern = java.util.regex.Pattern.compile("^\\+?[1-9][0-9]*$");
        java.util.regex.Matcher ticketnumSelectmatch = pattern.matcher(ticketnumSelect);
        if (!ticketnumSelectmatch.matches()) {
            information.setText("票个数, 请输入有效数字！");
            return -1;
        }
        return 0;
    }

