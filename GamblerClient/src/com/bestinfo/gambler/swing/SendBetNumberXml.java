    public AppHeader getheader(AppHeader head, javax.swing.JTextArea information) {
        AppHeader header = null;
        if (head == null) {
            AppHeader headtemp = StaticVariable.head;
            if (headtemp != null) {
                header = headtemp;
            } else {
                information.setText("未知的发送方类型！");
                logger.info("未知的发送方类型！");
                return null;
            }
        } else {
            header = head;
        }
        return header;
    }

