package cn.edugate.esb.pojo;

public class Sms {
	private String cmHost;  			//下行IP
	private String cmHostPort;			//下行端口
	private String wsHost;				//IP
	private String wsHostPort;			//端口
	private String userName;	//SMS用户名	
	private String passWord;	//SMS密码
	private String timeLimit;   //发送短信间隔时间
	public String getTimeLimit() {
		return timeLimit;
	}
	public void setTimeLimit(String timeLimit) {
		this.timeLimit = timeLimit;
	}
	public String getCmHost() {
		return cmHost;
	}
	public void setCmHost(String cmHost) {
		this.cmHost = cmHost;
	}
	public String getCmHostPort() {
		return cmHostPort;
	}
	public void setCmHostPort(String cmHostPort) {
		this.cmHostPort = cmHostPort;
	}
	public String getWsHost() {
		return wsHost;
	}
	public void setWsHost(String wsHost) {
		this.wsHost = wsHost;
	}
	public String getWsHostPort() {
		return wsHostPort;
	}
	public void setWsHostPort(String wsHostPort) {
		this.wsHostPort = wsHostPort;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPassWord() {
		return passWord;
	}
	public void setPassWord(String passWord) {
		this.passWord = passWord;
	}
	
	
}
