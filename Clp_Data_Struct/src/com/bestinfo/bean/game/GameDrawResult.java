package com.bestinfo.bean.game;

import java.io.Serializable;
import java.util.List;

/**
 * 开奖结果
 *
 * @author yangyuefu
 */
public class GameDrawResult implements Serializable{

    /**
	 * 
	 */
	private static final long serialVersionUID = -7322823063576999705L;
	private String luckyNo;//中奖号码
    private String specialNo;//特别奖号码
    private Long currentReleaseMoney;//当期发行彩票金额
    private Long currentPayMoney;//当期派彩金额
    private Long lastRollMoney;//上期奖金滚入金额
    private Long nextRollMoney;//滚入下期奖金金额
    private String operatorCode;//操作员编码
    private Integer cashValidDate;//兑奖有效日期时间

    private List<GameClassInfo> classList;//奖级奖金列表

    public List<GameClassInfo> getClassList() {
        return classList;
    }

    public void setClassList(List<GameClassInfo> classList) {
        this.classList = classList;
    }

    public String getLuckyNo() {
        return luckyNo;
    }

    public void setLuckyNo(String luckyNo) {
        this.luckyNo = luckyNo;
    }

    public String getSpecialNo() {
        return specialNo;
    }

    public void setSpecialNo(String specialNo) {
        this.specialNo = specialNo;
    }

    public Long getCurrentReleaseMoney() {
        return currentReleaseMoney;
    }

    public void setCurrentReleaseMoney(Long currentReleaseMoney) {
        this.currentReleaseMoney = currentReleaseMoney;
    }

    public Long getCurrentPayMoney() {
        return currentPayMoney;
    }

    public void setCurrentPayMoney(Long currentPayMoney) {
        this.currentPayMoney = currentPayMoney;
    }

    public Long getLastRollMoney() {
        return lastRollMoney;
    }

    public void setLastRollMoney(Long lastRollMoney) {
        this.lastRollMoney = lastRollMoney;
    }

    public Long getNextRollMoney() {
        return nextRollMoney;
    }

    public void setNextRollMoney(Long nextRollMoney) {
        this.nextRollMoney = nextRollMoney;
    }

    public String getOperatorCode() {
        return operatorCode;
    }

    public void setOperatorCode(String operatorCode) {
        this.operatorCode = operatorCode;
    }

    public Integer getCashValidDate() {
        return cashValidDate;
    }

    public void setCashValidDate(Integer cashValidDate) {
        this.cashValidDate = cashValidDate;
    }

}
