package com.bestinfo.bean.union;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * 联销-普通游戏中奖联销汇总(T_union_stat_prize)
 *
 * @author LH
 */
public class UnionStatPrize implements Serializable {

    private static final long serialVersionUID = 5588676111226159636L;

    /**
     * 省系统编号
     */
    private Integer province_id;

    /**
     * 分系统编号
     */
    private Integer system_id;

    /**
     * 游戏编号
     */
    private Integer game_id;

    /**
     * 总期号
     */
    private Integer draw_id;

    /**
     * 玩法编号
     */
    private Integer play_id;

    /**
     * 开奖次数
     */
    private Integer open_id;

    /**
     * 奖级编号
     */
    private Integer class_id;

    /**
     * 奖级名称
     */
    private String class_name;

    /**
     * 中奖注数
     */
    private Long prize_num;

    /**
     * 单注奖金
     */
    private BigDecimal stake_prize;

    public Integer getProvince_id() {
        return province_id;
    }

    public void setProvince_id(Integer province_id) {
        this.province_id = province_id;
    }

    public Integer getSystem_id() {
        return system_id;
    }

    public void setSystem_id(Integer system_id) {
        this.system_id = system_id;
    }

    public Integer getGame_id() {
        return game_id;
    }

    public void setGame_id(Integer game_id) {
        this.game_id = game_id;
    }

    public Integer getDraw_id() {
        return draw_id;
    }

    public void setDraw_id(Integer draw_id) {
        this.draw_id = draw_id;
    }

    public Integer getPlay_id() {
        return play_id;
    }

    public void setPlay_id(Integer play_id) {
        this.play_id = play_id;
    }

    public Integer getOpen_id() {
        return open_id;
    }

    public void setOpen_id(Integer open_id) {
        this.open_id = open_id;
    }

    public Integer getClass_id() {
        return class_id;
    }

    public void setClass_id(Integer class_id) {
        this.class_id = class_id;
    }

    public String getClass_name() {
        return class_name;
    }

    public void setClass_name(String class_name) {
        this.class_name = class_name;
    }

    public Long getPrize_num() {
        return prize_num;
    }

    public void setPrize_num(Long prize_num) {
        this.prize_num = prize_num;
    }

    public BigDecimal getStake_prize() {
        return stake_prize;
    }

    public void setStake_prize(BigDecimal stake_prize) {
        this.stake_prize = stake_prize;
    }

}
