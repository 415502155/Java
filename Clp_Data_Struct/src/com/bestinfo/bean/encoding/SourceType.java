package com.bestinfo.bean.encoding;

import java.io.Serializable;

/**
 * 资金来源类型
 *
 * @author user
 */
public class SourceType implements Serializable {

    /**
	 * 
	 */
	private static final long serialVersionUID = 1169833372159246816L;
	private String source_type;  //资金来源类型
    private Integer trade_type; //交易类型
    private String source_name; //资金来源类型名称
    private Integer work_status; //工作状态

    /**
     * @return the source_type
     */
    public String getSource_type() {
        return source_type;
    }

    /**
     * @param source_type the source_type to set
     */
    public void setSource_type(String source_type) {
        this.source_type = source_type;
    }

    /**
     * @return the trade_type
     */
    public Integer getTrade_type() {
        return trade_type;
    }

    /**
     * @param trade_type the trade_type to set
     */
    public void setTrade_type(Integer trade_type) {
        this.trade_type = trade_type;
    }

    /**
     * @return the source_name
     */
    public String getSource_name() {
        return source_name;
    }

    /**
     * @param source_name the source_name to set
     */
    public void setSource_name(String source_name) {
        this.source_name = source_name;
    }

    /**
     * @return the work_status
     */
    public Integer getWork_status() {
        return work_status;
    }

    /**
     * @param work_status the work_status to set
     */
    public void setWork_status(Integer work_status) {
        this.work_status = work_status;
    }

}
