package com.bestinfo.bean.game;

import java.io.Serializable;

/**
 * 信息-开奖公告
 *
 * @author hhhh
 */
public class OpenprizeInfo implements Serializable {

    /**
	 * 
	 */
	private static final long serialVersionUID = -5704960366439328324L;

	/**
     * 游戏编号
     */
    private Integer game_id;
    
    /**
     * 期号
     */
    private Integer draw_id;
    
    /**
     * keno期号
     */
    private Integer keno_draw_id;
    
    /**
     * 公告信息
     */
    private String openprize_info;
    
    /**
     * 数据摘要
     */
    private String openprize_md5;

    public Integer getGame_id() {
        return game_id;
    }

    public void setGame_id(Integer game_id) {
        this.game_id = game_id;
    }

    public Integer getDraw_id() {
        return draw_id;
    }

    public void setDraw_id(Integer draw_id) {
        this.draw_id = draw_id;
    }

    public Integer getKeno_draw_id() {
        return keno_draw_id;
    }

    public void setKeno_draw_id(Integer keno_draw_id) {
        this.keno_draw_id = keno_draw_id;
    }

    public String getOpenprize_info() {
        return openprize_info;
    }

    public void setOpenprize_info(String openprize_info) {
        this.openprize_info = openprize_info;
    }

    public String getOpenprize_md5() {
        return openprize_md5;
    }

    public void setOpenprize_md5(String openprize_md5) {
        this.openprize_md5 = openprize_md5;
    }


}
