package com.bestinfo.bean.game;

import java.io.Serializable;

/**
 * 彩票公告
 * @author YangRong
 */
public class TicketBulletin implements Serializable {
   /**
	 * 
	 */
	private static final long serialVersionUID = 4082053336557159090L;
private  Integer game_id;//游戏编号
   private  Integer line_no;//行号
   private  String  bulletin;//公告
   private  Integer bulletin_len;//公告长度
   private  Integer work_status;//使用状态

    public Integer getGame_id() {
        return game_id;
    }

    public void setGame_id(Integer game_id) {
        this.game_id = game_id;
    }

    public Integer getLine_no() {
        return line_no;
    }

    public void setLine_no(Integer line_no) {
        this.line_no = line_no;
    }

    public String getBulletin() {
        return bulletin;
    }

    public void setBulletin(String bulletin) {
        this.bulletin = bulletin;
    }

    public Integer getBulletin_len() {
        return bulletin_len;
    }

    public void setBulletin_len(Integer bulletin_len) {
        this.bulletin_len = bulletin_len;
    }

    public Integer getWork_status() {
        return work_status;
    }

    public void setWork_status(Integer work_status) {
        this.work_status = work_status;
    }
   
    
}
