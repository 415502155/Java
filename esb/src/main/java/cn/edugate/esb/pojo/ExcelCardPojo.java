package cn.edugate.esb.pojo;

import java.io.Serializable;

public class ExcelCardPojo implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String org_name_cn;
	private String grade_name;
	private String clas_name;
	private String stud_name;
	private String cards;
	private String message;
	
	public String getOrg_name_cn() {
		return org_name_cn;
	}
	public void setOrg_name_cn(String org_name_cn) {
		this.org_name_cn = org_name_cn;
	}
	public String getGrade_name() {
		return grade_name;
	}
	public void setGrade_name(String grade_name) {
		this.grade_name = grade_name;
	}
	public String getClas_name() {
		return clas_name;
	}
	public void setClas_name(String clas_name) {
		this.clas_name = clas_name;
	}
	public String getStud_name() {
		return stud_name;
	}
	public void setStud_name(String stud_name) {
		this.stud_name = stud_name;
	}
	public String getCards() {
		return cards;
	}
	public void setCards(String cards) {
		this.cards = cards;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	
	
}
