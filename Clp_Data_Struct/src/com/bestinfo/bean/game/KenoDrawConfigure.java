package com.bestinfo.bean.game;

import java.io.Serializable;

/**
 * 游戏-快开游戏期参数配置表(T_keno_draw_configure)
 *
 * @author yangyuefu
 */
public class KenoDrawConfigure implements Serializable{

    /**
	 * 
	 */
	private static final long serialVersionUID = 6039634659020163184L;
	private Integer game_id;//
    private Integer keno_draw_no;//keno期序号
    private String begin_time;//开始时间
    private String end_time;//结束时间
    private Integer draw_length;//本期期长
    private Integer multi_keno_num;//本期多期期数
    private Integer bulletin_time;//期开始到预计开奖公告发布时间
    private Integer re_bulletin_time;//重复取公告间隔s
    private Integer work_status;//工作状态
    private String note;//备注

    public Integer getGame_id() {
        return game_id;
    }

    public void setGame_id(Integer game_id) {
        this.game_id = game_id;
    }

    public Integer getKeno_draw_no() {
        return keno_draw_no;
    }

    public void setKeno_draw_no(Integer keno_draw_no) {
        this.keno_draw_no = keno_draw_no;
    }

    public String getBegin_time() {
        return begin_time;
    }

    public void setBegin_time(String begin_time) {
        this.begin_time = begin_time;
    }

    public String getEnd_time() {
        return end_time;
    }

    public void setEnd_time(String end_time) {
        this.end_time = end_time;
    }

    public Integer getDraw_length() {
        return draw_length;
    }

    public void setDraw_length(Integer draw_length) {
        this.draw_length = draw_length;
    }

    public Integer getMulti_keno_num() {
        return multi_keno_num;
    }

    public void setMulti_keno_num(Integer multi_keno_num) {
        this.multi_keno_num = multi_keno_num;
    }

    public Integer getBulletin_time() {
        return bulletin_time;
    }

    public void setBulletin_time(Integer bulletin_time) {
        this.bulletin_time = bulletin_time;
    }

    public Integer getRe_bulletin_time() {
        return re_bulletin_time;
    }

    public void setRe_bulletin_time(Integer re_bulletin_time) {
        this.re_bulletin_time = re_bulletin_time;
    }

    public Integer getWork_status() {
        return work_status;
    }

    public void setWork_status(Integer work_status) {
        this.work_status = work_status;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

}
