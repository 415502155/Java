    private void sure1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sure1ActionPerformed
        // TODO add your handling code here:
        AppHeader head = StaticVariable.head;
         if (head == null) {
            logger.error("没有初始化信息！");
           return;
        } 
        head = new AppHeader();
        head.setType(Integer.parseInt(terminal_id4.getText()));//发送方类型    自助终端运营商4
        head.setVersion(0);//version版本号(填0)
        head.setDealer_id(terminal_id.getText());//head.setDealer_id("111");//运营商ID
        head.setTerminal_id(Integer.parseInt(terminal_id3.getText()));//head.setTerminal_id("111");//终端机号
        head.setMobile(terminal_id2.getText());//手机号(填0)head.setMobile("15101105612");
        head.setPhone(terminal_id1.getText());//电话号码(填0)   head.setPhone("15101105612");
        head.setSent_time(new Date().toString());//发送时间head.setSent_time("2015-07-29 12:12:12");
        //head.setSign(new byte[0]);//head.setSign("1231321".getBytes());//报文体签名
        
       
        String size = gameids1.getText();
        if(size.isEmpty()){
            logger.error("线程数不能为空！");
            return;
        }
        String num1 = num.getText();
        if(num1.isEmpty()){
            logger.error("个数不能为空！");
            return;
        }
        try{
            new SysTime().morethread(head,Integer.parseInt(size),Integer.parseInt(num1));
        }catch(NumberFormatException e){
            logger.error("",e);
        }
        System.out.println("send over");
    }//GEN-LAST:event_sure1ActionPerformed

