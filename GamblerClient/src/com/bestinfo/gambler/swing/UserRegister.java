    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        // TODO add your handling code here:
        AppHeader header = getHead();
        if (header == null) {
            return;
        }
        boolean threadis = thread.isSelected();
        String dealerS = "0";
        boolean binddealerb = bingdealer.isSelected();
        int ibind = 0;
        if (binddealerb) {//绑定运营商
            ibind = 1;
            dealerS = header.getDealer_id();
        }

        String keyS = key.getText();
        String usernoS = userno.getText();
        if (keyS == null || keyS.isEmpty()) {
            information.setText("关键字不为空！");
            return;
        }
        String eno = eachno.getText();
        String re = CommTool.examinenumber(usernoS);
        if (re != null) {
            information.setText(re);
            return;
        }
        boolean testif = wrongtest.isSelected();
        GamblerRegistReq grreq = getUser(ibind, dealerS);
        try {
            new CreateUserDataAndSend().sendUserXml(header, grreq, keyS, Integer.parseInt(usernoS), threadis, testif, Integer.parseInt(eno));
            information.setText("完毕！");
        } catch (NumberFormatException e) {
            information.setText(e.toString());
        }
        System.out.println("send over");
    }//GEN-LAST:event_sendActionPerformed

    private void send1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_send1ActionPerformed
        // TODO add your handling code here:           
        AppHeader header = getHead();
        if (header == null) {
            return;
        }
        String userName = username.getText();
        String userpwd = password.getText();
        if (userName.isEmpty()) {
            information.setText("用户名为空！");
            return;
        }
        if (userpwd.isEmpty()) {
            information.setText("密码为空！");
            return;
        }
        String dealerS = "0";
        boolean binddealerb = bingdealer1.isSelected();
        int ibind = 0;
        if (binddealerb) {
            ibind = 1;
            dealerS = header.getDealer_id();
        }
        boolean testif = wrongtest.isSelected();
        GamblerRegistReq grreq = getUser(ibind, dealerS);
        grreq.setGamblerName(userName);
        grreq.setGamblerPwd(userpwd);
        try {
            new CreateUserDataAndSend().SendXmlUserInfo(header, grreq, testif);
        } catch (Exception e) {
            information.setText(e.toString());
            return;
        }
        information.setText("发送成功！");
        System.out.println("send over");
    }//GEN-LAST:event_send1ActionPerformed

