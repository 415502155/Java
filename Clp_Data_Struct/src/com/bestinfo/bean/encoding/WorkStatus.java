package com.bestinfo.bean.encoding;

import java.io.Serializable;

/**
 * 工作状态
 *
 * @author chenliping
 */
public class WorkStatus implements Serializable{

    /**
	 * 
	 */
	private static final long serialVersionUID = 401300244496946184L;
	private Integer work_status;//启用
    private String work_status_name;//不启用

    public Integer getWork_status() {
        return work_status;
    }

    public void setWork_status(Integer work_status) {
        this.work_status = work_status;
    }

    public String getWork_status_name() {
        return work_status_name;
    }

    public void setWork_status_name(String work_status_name) {
        this.work_status_name = work_status_name;
    }
}
