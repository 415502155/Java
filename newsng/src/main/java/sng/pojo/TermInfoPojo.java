package sng.pojo;

import java.util.Date;
/**
 * 学期列表信息列属性
 * @author magq
 *
 */
public class TermInfoPojo {
	/**
	 * 学期主键
	 */
	private Integer term_id;
	/**
	 * 机构主键
	 */
	private Integer org_id;
	/**
	 * 学期名称
	 */
	private String term_name;
	/**
	 * 所属年份
	 */
	private String cur_year;
	/**
	 * 学期类型 (1:学期;2:假期)
	 */
	private String term_type;
	/***
	 * 开始时间
	 */
	private String start_time;
	
	/**
	 * 编辑 删除按钮是否在操作列中显示
	 */
	private boolean isOperate;

	public Integer getTerm_id() {
		return term_id;
	}

	public void setTerm_id(Integer term_id) {
		this.term_id = term_id;
	}

	public Integer getOrg_id() {
		return org_id;
	}

	public void setOrg_id(Integer org_id) {
		this.org_id = org_id;
	}

	public String getTerm_name() {
		return term_name;
	}

	public void setTerm_name(String term_name) {
		this.term_name = term_name;
	}

	public String getCur_year() {
		return cur_year;
	}

	public void setCur_year(String cur_year) {
		this.cur_year = cur_year;
	}

	

	public String getTerm_type() {
		return term_type;
	}

	public void setTerm_type(String term_type) {
		this.term_type = term_type;
	}

	

	public String getStart_time() {
		return start_time;
	}

	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}

	public boolean isOperate() {
		return isOperate;
	}

	public void setOperate(boolean isOperate) {
		this.isOperate = isOperate;
	}

	@Override
	public String toString() {
		return "TermInfoPojo [term_id=" + term_id + ", org_id=" + org_id + ", term_name=" + term_name + ", cur_year="
				+ cur_year + ", term_type=" + term_type + ", start_time=" + start_time + ", isOperate=" + isOperate
				+ "]";
	}
	
	
	

}
