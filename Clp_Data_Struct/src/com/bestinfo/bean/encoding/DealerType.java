package com.bestinfo.bean.encoding;

import java.io.Serializable;

/**
 * 代销商类型
 *
 * @author user
 */
public class DealerType implements Serializable {

    /**
	 * 
	 */
	private static final long serialVersionUID = -2795662823193580578L;
	private Integer dealer_type;  //代销商类型
    private String dealer_type_name; //交易类型
    private Integer work_status; //工作状态

    /**
     * @return the dealer_type
     */
    public Integer getDealer_type() {
        return dealer_type;
    }

    /**
     * @param dealer_type the dealer_type to set
     */
    public void setDealer_type(Integer dealer_type) {
        this.dealer_type = dealer_type;
    }

    /**
     * @return the dealer_type_name
     */
    public String getDealer_type_name() {
        return dealer_type_name;
    }

    /**
     * @param dealer_type_name the dealer_type_name to set
     */
    public void setDealer_type_name(String dealer_type_name) {
        this.dealer_type_name = dealer_type_name;
    }

    /**
     * @return the work_status
     */
    public Integer getWork_status() {
        return work_status;
    }

    /**
     * @param work_status the work_status to set
     */
    public void setWork_status(Integer work_status) {
        this.work_status = work_status;
    }
}
