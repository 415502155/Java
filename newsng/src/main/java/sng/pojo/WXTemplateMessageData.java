package sng.pojo;

import java.io.Serializable;

public class WXTemplateMessageData implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3981906175698167439L;

	private String value;

	// 字体颜色
	private String color = "#093d89";

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public WXTemplateMessageData() {
	}

	public WXTemplateMessageData(String value) {
		this.value = value;
	}
	
	public WXTemplateMessageData(String value, String color) {
		this.value = value;
		this.color = color;
	}
}
