    private AppHeader getHead() {
        String typeSelect = type.getText();
        String dealer_idS = dealer_id.getText();
        String terminal_idS = terminal_id.getText();
        String mobileS = mobile.getText();
        String phoneS = phone.getText();
        if (CommTool.examinenumber(typeSelect) != null) {
            information.setText("发送方类型不正确");
            return null;
        }
        if (dealer_idS.equals("") && terminal_idS.equals("") && mobileS.equals("") && phoneS.equals("")) {
            information.setText("参数错误！");
            return null;
        }
        head = new AppHeader();
        head.setType(Integer.parseInt(typeSelect));
        head.setDealer_id(dealer_idS);
        head.setTerminal_id(Integer.parseInt(terminal_idS));
        head.setMobile(mobileS);
        head.setPhone(phoneS);
        //head.setSign("1231321".getBytes());

        return head;
    }

