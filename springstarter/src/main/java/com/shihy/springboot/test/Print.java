public class Print extends Output {
	
	private String name = "print";
	
	public Print() {
		aa();
		bb();
	}
	@Override
	public void aa() {
		System.out.println("111111111111111111111111111" + name);
	}
	@Override
	public void bb() {
		System.out.println("222222222222222222222222222" + name);
	}
	
	public static void main(String[] args) {
		new Print();
	}

}

class Output{
	private String name = "output";
	
	public Output() {
		aa();
		bb();
	}
	
	public void aa() {
		System.out.println("33333333333333333333333" + name);
	}
	
	public void bb() {
		System.out.println("44444444444444444444444" + name);
	}
}