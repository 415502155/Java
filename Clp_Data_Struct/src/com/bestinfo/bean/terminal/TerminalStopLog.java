package com.bestinfo.bean.terminal;

import java.io.Serializable;
import java.util.Date;

/**
 * 封机历史日志表
 *
 * @author hhhh
 */
public class TerminalStopLog implements Serializable {

    /**
     *
     */
    private static final long serialVersionUID = 1738082177535707170L;
    private Integer terminal_id;//终端编号
    private Date stop_time;//封机时间
    private Date continue_time;//解除封机时间
    private String stop_reason;//封机原因
    private Integer reason_type;//封机类型
    private Integer auto_continue;//自动开机
    private Integer user_id;//操作员编号
    private Integer stop_status;//封机状态
    private Integer unlock_user_id;//解封操作员

    private String continue_time_str;//解除封机时间
    private String stop_time_str;//封机时间

    public Integer getTerminal_id() {
        return terminal_id;
    }

    public void setTerminal_id(Integer terminal_id) {
        this.terminal_id = terminal_id;
    }

    public Date getStop_time() {
        return stop_time;
    }

    public void setStop_time(Date stop_time) {
        this.stop_time = stop_time;
    }

    public Date getContinue_time() {
        return continue_time;
    }

    public void setContinue_time(Date continue_time) {
        this.continue_time = continue_time;
    }

    public String getStop_reason() {
        return stop_reason;
    }

    public void setStop_reason(String stop_reason) {
        this.stop_reason = stop_reason;
    }

    public Integer getReason_type() {
        return reason_type;
    }

    public void setReason_type(Integer reason_type) {
        this.reason_type = reason_type;
    }

    public Integer getAuto_continue() {
        return auto_continue;
    }

    public void setAuto_continue(Integer auto_continue) {
        this.auto_continue = auto_continue;
    }

    public Integer getUser_id() {
        return user_id;
    }

    public void setUser_id(Integer user_id) {
        this.user_id = user_id;
    }

    public Integer getStop_status() {
        return stop_status;
    }

    public void setStop_status(Integer stop_status) {
        this.stop_status = stop_status;
    }

    public Integer getUnlock_user_id() {
        return unlock_user_id;
    }

    public void setUnlock_user_id(Integer unlock_user_id) {
        this.unlock_user_id = unlock_user_id;
    }

    public String getContinue_time_str() {
        return continue_time_str;
    }

    public void setContinue_time_str(String continue_time_str) {
        this.continue_time_str = continue_time_str;
    }

    public String getStop_time_str() {
        return stop_time_str;
    }

    public void setStop_time_str(String stop_time_str) {
        this.stop_time_str = stop_time_str;
    }

}
